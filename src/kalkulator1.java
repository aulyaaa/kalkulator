    private void samadenganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_samadenganActionPerformed
        // TODO add your handling code here:
        switch(pilih){
            case 1:
                angka2 = Double.parseDouble(angka);
                jumlah = angka1 + angka2;
                angka = Double.toString(jumlah);
                hasil.setText(angka);
                break;
            case 2:
                angka2 = Double.parseDouble(angka);
                jumlah = angka1 - angka2;
                angka = Double.toString(jumlah);
                hasil.setText(angka);
                break;
            case 3:
                angka2 = Double.parseDouble(angka);
                jumlah = angka1 / angka2;
                angka = Double.toString(jumlah);
                hasil.setText(angka);
                break;
            case 4:
                angka2 = Double.parseDouble(angka);
                jumlah = angka1 * angka2;
                angka = Double.toString(jumlah);
                hasil.setText(angka);
                break;
            case 5:
                angka2 = Double.parseDouble(angka);
                jumlah = angka1 % angka2;
                angka = Double.toString(jumlah);
                hasil.setText(angka);
                break;
            case 6:
                jumlah = Math.sqrt(angka1);
                angka = Double.toString(jumlah);
                hasil.setText(angka);
                break;
            case 7:
                angka2 = Double.parseDouble(angka);
                jumlah = Math.pow(angka1, angka2);
                angka = Double.toString(jumlah);
                hasil.setText(angka);
                break;
            case 8:
                jumlah = angka1 / 100;
                angka = Double.toString(jumlah);
                hasil.setText(angka);
                break;
                default:
                break;
        }
    }//GEN-LAST:event_samadenganActionPerformed

